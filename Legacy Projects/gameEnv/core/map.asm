:map_highland_number	dat 0x0
:map_river_number		dat 0x0
:map_max_highland		dat 0x28
:map_max_river			dat 0x28

:map_generate
set push, i
set push, j
set push, y
set i, 0
set j, map_tile_entries
:map_generate_loop
ife i, 0x180
	set pc, map_generate_end
jsr random_extract_number
ifl y, 0x800
	ifl [map_river_number], [map_max_river]
			jsr map_set_river
ifl y, 0x1000
	ifg y, 0x7FF
		ifl [map_highland_number], [map_max_highland]
			jsr map_set_highland
ife [j], 0x20
	bor [j], 0x0200		;plain
add i, 1
add j, 1
set pc, map_generate_loop
:map_generate_end
set y, pop
set j, pop
set i, pop
set pc, pop

:map_set_highland
xor [j], 0x0600		;highland
add [map_highland_number], 1
set pc, pop

:map_set_river
xor [j], 0x0100		;river
add [map_river_number], 1
set pc, pop

:map_set_road
set push, b
set push, c
set b, [j]
and b, 0xF0FF
set c, [j]
and c, 0xF00
ife c, 0x200
	set c, 0x700
ife c, 0x600
	set c, 0x800
bor b, c
set [j], b
set pc, pop

:map_get_terrain	;A, B - pos
set push, a
set push, b
mul b, 0x20
add b, a
set a, [b]
and a, 0x100
shr a, 0x8
set c, a
set b, pop
set a, pop
set pc, pop